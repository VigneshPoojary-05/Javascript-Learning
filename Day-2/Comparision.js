// Loosely Equal and Not Equals
console.log(5==5);
console.log(5=="5")
console.log(3=="5")

console.log(5!=5);
console.log(5!=="5")
console.log(3!=="5")
console.log(5!="3")


// Strickly Equals and Not Equals
console.log(5===5)
console.log(5==="5")
console.log("3"==="5")

console.log(5!=="5")
console.log(5!==5)